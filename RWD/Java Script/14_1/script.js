function redSignal(){
    const rd = document.getElementById('red');
    rd.style.background = 'red';
    rd.innerHTML = "Stop!!!";
}

function greenSignal(){
    const grn = document.getElementById('green');
    grn.style.background = 'green';
}

function orangeSignal(){
    const org = document.getElementById('orange');
    org.style.background = 'orange';
}